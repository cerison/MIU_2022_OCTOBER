    public void loadListOfOverdue(String isbn) {
        tableModel.setRowCount(0);
        List<CheckoutEntry> entries = ci.getCheckedoutEntries(isbn);
        for (CheckoutEntry entry : entries) {
            this.tableModel.insertRow(
                    tableModel.getRowCount(),
                    new Object[]{
                        entry.getBookCopy().getBook().getTitle(),
                        entry.getBookCopy().getBook().getIsbn(),
                        entry.getBookCopy().getCopyNum(),
                        entry.getCheckoutRecord().getLibraryMember().getMemberId(),
//                        entry.getDueDate(),
                        entry.getBookCopy().isAvailable() ? "Yes" : "No",
                        entry.getDueDate().isBefore(LocalDate.now()) && !entry.getBookCopy().isAvailable() ? "Yes" : "No"
                    }
            );
        }
    }

