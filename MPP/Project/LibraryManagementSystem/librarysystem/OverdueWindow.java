    private void navigateToBookWindow() { //// This method navigates to the BookWindow.
        this.setVisible(false);     // It first sets the current window's visibility to false.
        BookWindow bookWindow = new BookWindow(); // Then it creates an instance of the BookWindow.

        FrameDragListenerUtil frameDragListener = new FrameDragListenerUtil(bookWindow); // It also attaches a FrameDragListenerUtil to the bookWindow, making it draggable by the user.
        bookWindow.addMouseListener(frameDragListener);
        bookWindow.addMouseMotionListener(frameDragListener);
        bookWindow.setLocationRelativeTo(null);
        bookWindow.setVisible(true); // Then it centers the bookWindow on the screen and sets its visibility to true.
    }
